                    <p>
                        Like many industrialized nations, Mexico has a comprehensive legal and statutory Immigration Policy affecting Mexicans and foreign nationals.
                    </p>
                    <p><b>FMT or Tourist Visa. </b> Tourist visas are intended for visitors to Mexico on short term visits (six months or less); usually tourists and short-term visitors. For trips of longer than six months, other visa should be considered. The FMT has a 'Business Visitor' section, specifically for persons who come to Mexico for short business visits not exceeding thirty days. FMT visas are issued by airlines and are also available at ports of entry.</p>
                    <p><b>FM3 (The Long-Term Non-Immigrant Visa).</b> Mexico operates what is known as a FM3 Visa. The FM3 visa is a renewable long term (more than six months) permit which gives non-immigrant temporary residency status to the holder. This means that it gives a person the right to live in Mexico (under terms as set out in the visa) but it does not lead to, and cannot be converted to, a visa leading to permanent residency.</p>
                    <p>There are various categories under which FM3 visas are granted, and these relate to the activities you intend to undertake while in Mexico. Under the terms of the FM3, you are authorized to only undertake certain, specific activities which may be lucrative or non-lucrative, depending on the visa's classification.</p>
                    <p><b>FM2 (The Immigrant Visa).</b> These visas are intended for people seeking permanent residency status in Mexico or those seeking eventual Mexican Citizenship.</p>
                    <p>There are various categories under which FM2 visas are granted, and these relate to the activities you intend to undertake while in Mexico. Under the terms of the FM2, you are authorized to only undertake certain, specific activities which may be lucrative or non-lucrative.</p>
                    <p>You must hold a FM2 for a qualifying period before you may apply for "immigrant" status or Mexican Citizenship. You do not need to have held a FM3 visa before applying for a FM2, and any years you may have accrued while living in Mexico under the auspice of a FM3 permit do not count towards your FM2 qualification period.</p>
                    <p>If your goal is to get the long term residency in Mexico or to become a Mexican Citizen, you should apply for FM2 Visa (or request a change of status from FM3 to FM2) so that your time starts counting towards the qualification period as soon as possible.</p>
